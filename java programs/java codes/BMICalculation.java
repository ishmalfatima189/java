public class BMICalculation{
public static void main(String[] args) {
 int weight = 62;
 double height = 1.68;
 double bmi = weight / (height * height);
 System.out.println("Weight: " + weight + " kg");
 System.out.println("Height: " + height + " m");
 System.out.println("BMI: " + bmi);
 }
}